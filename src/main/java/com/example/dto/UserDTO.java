package com.example.dto;

import lombok.Data;

/**
 * DTO (Data Transfer Object) для передачі даних користувача при реєстрації.
 * <p>
 * Використовується для інкапсуляції вхідних даних: імені, електронної пошти та пароля.
 * Не містить жодної бізнес-логіки, лише поля для передачі інформації між рівнями додатку.
 */
@Data
public class UserDTO {

  /**
   * Ім'я користувача.
   */
  private String name;

  /**
   * Електронна пошта користувача.
   */
  private String email;

  /**
   * Пароль користувача (у відкритому вигляді, перед хешуванням).
   */
  private String password;
}
